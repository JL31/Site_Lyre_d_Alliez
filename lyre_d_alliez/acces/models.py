# =================
class Jeton(Model):
    """
        Classe qui décrit le modèle de jeton qui sera utilisé
        pour la gestion des liens à usage unique (et limité dans le temps)
        dans le cadre de la création d'un profil membre
    """

    valeur = TextField(null=False, blank=False, max_length=20)
    deja_utilise = BooleanField(null=False, blank=False, default=False)

    # =========
    class Meta:
        """
            Configuration/définition des options de metadonnées du modèle
        """

        verbose_name = "jeton"


